// --- Balanced top-level entry extractor (handles nested { } inside `e:[...]`). ---
function* iterateEntries(txt) {
  let depth = 0, start = -1, inStr = false, strCh = "";
  for (let i = 0; i < txt.length; i++) {
    const c = txt[i];
    if (inStr) {
      if (c === "\\") { i++; continue; }
      if (c === strCh) inStr = false;
      continue;
    }
    if (c === '"' || c === "'") { inStr = true; strCh = c; continue; }
    if (c === "{") { if (depth === 0) start = i; depth++; }
    else if (c === "}") {
      depth--;
      if (depth === 0) yield { start, end: i, body: txt.slice(start, i + 1) };
    }
  }
}

function patchFile(filePath) {
  const orig = fs.readFileSync(filePath, "utf8");
  const edits = []; // {pos, insert}
  let patched = 0, skipped = 0;
  const unknown = [];

  for (const { end, body } of iterateEntries(orig)) {
    if (!/c:"動"/.test(body)) continue;
    if (/,t:"/.test(body)) continue; // already has t
    const w = (body.match(/w:"([^"]+)"/) || [])[1];
    const cls = MAP[w];
    if (!cls) {
      unknown.push(w);
      skipped++;
      continue;
    }
    // Insert `,t:"X"` just before the closing `}` at index `end`.
    edits.push({ pos: end, insert: `,t:"${cls}"` });
    patched++;
  }

  // Apply edits in reverse so positions stay valid.
  let out = orig;
  edits.sort((a, b) => b.pos - a.pos);
  for (const { pos, insert } of edits) {
    out = out.slice(0, pos) + insert + out.slice(pos);
  }

  if (!DRY && out !== orig) fs.writeFileSync(filePath, out);
  return { patched, skipped, unknown };
}

