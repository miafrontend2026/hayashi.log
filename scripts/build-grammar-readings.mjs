#!/usr/bin/env node
// 把 clean.json 包成 window.GRAMMAR_KANJI_READINGS，直接 <script src> 載入。
import fs from 'node:fs';
import path from 'node:path';

const ROOT = path.resolve(import.meta.dirname, '..');
const SRC = path.join(ROOT, 'scripts/grammar-kanji-readings.clean.json');
const OUT = path.join(ROOT, 'grammar-kanji-readings.js');

const data = JSON.parse(fs.readFileSync(SRC, 'utf8'));
const js = `// Generated by scripts/build-grammar-readings.mjs — 來源 grammar-kanji-readings.tsv 經 validate-readings.mjs 篩選\nwindow.GRAMMAR_KANJI_READINGS = ${JSON.stringify(data)};\n`;
fs.writeFileSync(OUT, js);
console.log(`wrote ${Object.keys(data).length} entries → ${path.relative(ROOT, OUT)}`);
